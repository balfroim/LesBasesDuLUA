--[[ EPISODE 2
  Une variable permets de retenir des informations.
  Elle associe un nom(1) à une valeur.
  
  On peut lire et modifier(2) cette valeur en connaissant le nom de la variable.
  
  Pour définir une variable : nom = valeur
  
  ------------------------------
  (1) Unique et ne doit pas être un des mot-clés reservé de lua : 
     and       break     do        else      elseif
     end       false     for       function  if
     in        local     nil       not       or
     repeat    return    then      true      until     while
  (2) Dans d'autres langages on peut définir des variables constantes, mais pas en lua (du moins pas nativement (voir épisode 6))
--]]