--[[EPISODE 4 - BOUCLE
  Une boucle permets tout simplement de répéter plusieurs fois une même partie de code.
  
  En lua, il y a plusieurs types de boucles :
  * while : répéter tant qu'une condition est vraie (ex: tant que joueur est vivant)
  * repeat-until : répéter jusqu'à qu'une condition soit vraie (ex: jusqu'à que l'utilisateur entre un nombre positif)
  * for (numeric) : répéter pour chaque nombre dans un interval. (ex: pour tout les nombres de 1 à 10)
  * for (generic) : répéter pour chaque valeur d'un itérateur (ex: pour tout élément d'une table) (voir épisode 6)
]]--